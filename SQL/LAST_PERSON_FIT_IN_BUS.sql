SELECT * FROM QUEUE ORDER BY TURN ASC;


WITH FINAL AS (
SELECT PERSON_ID, PERSON_NAME, WEIGHT, TURN, SUM(WEIGHT) OVER (ORDER BY TURN) AS
TOTAL_WEIGHT FROM QUEUE
)
SELECT TOP 1 PERSON_NAME FROM FINAL WHERE TOTAL_WEIGHT<=1000 ORDER BY TOTAL_WEIGHT DESC;
