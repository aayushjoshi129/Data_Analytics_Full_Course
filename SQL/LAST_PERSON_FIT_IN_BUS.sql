CREATE DATABASE LAST_PERSON_FIT_IN_BUS;

USE LAST_PERSON_FIT_IN_BUS;

CREATE TABLE Queue (
    person_id INT PRIMARY KEY,
    person_name VARCHAR(100),
    weight INT,
    turn INT
);

